public class Switch_Statement
{
    public static void main(String[]args)
    {
        int n=10;
        switch(n)
        {
            case 10:
                System.out.println("=10");
                break;
            case 30:
                System.out.println("=30");
                break;
            case 90:
                System.out.println("=90");
        }
    }
}
